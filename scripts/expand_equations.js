// Build formatted JS array string
function fmt(eqList) {
  return eqList.map(e => {
    const r = JSON.stringify(e.r).replace(/"/g, '"');
    const p = JSON.stringify(e.p).replace(/"/g, '"');
    return `    { reactants: ${r}, products: ${p} }`;
  }).join(',\n');
}

